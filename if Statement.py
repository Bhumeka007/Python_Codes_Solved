def ifStat():
    x = input("x: ")
    y = input("y: ")
    z = input("z: ")
    if x>0:
        print("x is Positive")
    elif x<0:
        print("x is Negative")
    else:
        print("x is Zero")

    if y>0:
        print("y is Positive")
    elif y<0:
        print("y is Negative")
    else:
        print("y is Zero")

    if z>0:
        print("z is Positive")
    elif z<0:
        print("z is Negative")
    else:
        print("z is Zero")
